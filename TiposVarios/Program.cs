        {
            int i = 10;
            uint ui = -10U;
            long l = 100L;
            ulong ul = -100UL;
            double d = 12.345;
            float f = 1.2345f;
            decimal de = 0.1234m;

            Console.WriteLine(i);
            Console.WriteLine(ui);
            Console.WriteLine(l);
            Console.WriteLine(ul);
            Console.WriteLine(d);
            Console.WriteLine(f);
            Console.WriteLine(de);
            
        }
    }
}
